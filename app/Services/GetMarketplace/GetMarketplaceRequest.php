<?php

namespace App\Services\GetMarketplace;

class GetMarketplaceRequest
{
    private ?int $id;

    /**
     * @param int|null $id
     */
    public function __construct(?int $id)
    {
        $this->id = $id;
    }

    /**
     * @return int|null
     */
    public function getId(): ?int
    {
        return $this->id;
    }
}
